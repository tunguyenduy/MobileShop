package ptit.ndtu.mobileshop.entities;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;

/**
 *
 * @author NDT;
 */
@Entity
@Table(name = "NewsCategory")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "NewsCategory.findAll", query = "SELECT n FROM NewsCategory n")
    , @NamedQuery(name = "NewsCategory.findByCategoryId", query = "SELECT n FROM NewsCategory n WHERE n.categoryId = :categoryId")
    , @NamedQuery(name = "NewsCategory.findByCategoryName", query = "SELECT n FROM NewsCategory n WHERE n.categoryName = :categoryName")
    , @NamedQuery(name = "NewsCategory.findByStatus", query = "SELECT n FROM NewsCategory n WHERE n.status = :status")})
public class NewsCategory implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "category_id")
    private Integer categoryId;
    @Basic(optional = false)
    @Column(name = "category_name")
    private String categoryName;
    @Column(name = "status")
    private Integer status;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "categoryId")
    private List<News> newsList;

    public NewsCategory() {
    }

    public NewsCategory(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public NewsCategory(Integer categoryId, String categoryName) {
        this.categoryId = categoryId;
        this.categoryName = categoryName;
    }

    public Integer getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Integer categoryId) {
        this.categoryId = categoryId;
    }

    public String getCategoryName() {
        return categoryName;
    }

    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    @XmlTransient
    public List<News> getNewsList() {
        return newsList;
    }

    public void setNewsList(List<News> newsList) {
        this.newsList = newsList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (categoryId != null ? categoryId.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof NewsCategory)) {
            return false;
        }
        NewsCategory other = (NewsCategory) object;
        if ((this.categoryId == null && other.categoryId != null) || (this.categoryId != null && !this.categoryId.equals(other.categoryId))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.NewsCategory[ categoryId=" + categoryId + " ]";
    }
    
}
